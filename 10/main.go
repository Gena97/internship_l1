// 10.  Дана последовательность температурных колебаний: -25.4, -27.0 13.0, 19.0, 15.5, 24.5, -21.0, 32.5.
// Объединить данные значения в группы с шагом в 10 градусов.
// Последовательность в подмножноствах не важна.
// Пример: -20:{-25.0, -27.0, -21.0}, 10:{13.0, 19.0, 15.5}, 20: {24.5}, etc.

package main

import (
	"fmt"
	"math"
)

func main() {
	temperatures := []float64{-25.4, -27.0, 13.0, 19.0, 15.5, 24.5, -21.0, 32.5}

	groups := make(map[int][]float64)

	// Проходим по всем значениям температур
	for _, temp := range temperatures {
		// Определяем ключ группировки для текущей температуры
		groupKey := int(math.Floor(temp/10)) * 10
		// Добавляем текущую температуру в соответствующую группу
		groups[groupKey] = append(groups[groupKey], temp)
	}

	// Выводим группы температур
	for groupKey, groupValues := range groups {
		fmt.Printf("%d: %v\n", groupKey, groupValues)
	}
}
